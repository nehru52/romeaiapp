#!/usr/bin/env python3
"""Generate the Lloyd-Max centroid header for PolarQuant Q4.

Emits a const float[16] of MSE-optimal scalar quantizer levels for the
N(0, 1) distribution to stdout, in the exact form the C code expects.
The algorithm is the iterative conditional-expectation solver described
in PolarQuant (arXiv:2603.29078) and matched bit-for-bit against the
Python reference at
``packages/training/scripts/quantization/polarquant/polar_quant.py``.

Usage:
    python gen_centroids.py > include/polarquant/polar_centroids.h

Regeneration must produce a byte-identical file; the iteration is
deterministic for a fixed (n_levels, n_iter, initial boundaries) tuple.
"""

from __future__ import annotations

import sys

import numpy as np
from scipy.stats import norm


# Must match polar_quant.py:
#   _compute_lloyd_max_centroids(n_levels=16, n_iter=100)
N_LEVELS = 16
N_ITER = 100


def compute_centroids(n_levels: int, n_iter: int) -> np.ndarray:
    """Iterative Lloyd-Max for the standard normal distribution.

    Conditional expectation in [a, b] for X ~ N(0, 1):
        E[X | a < X < b] = (phi(a) - phi(b)) / (Phi(b) - Phi(a))
    """
    boundaries = np.linspace(-4.0, 4.0, n_levels + 1)
    boundaries[0] = -np.inf
    boundaries[-1] = np.inf

    centroids = np.zeros(n_levels)
    for _ in range(n_iter):
        for i in range(n_levels):
            a, b = boundaries[i], boundaries[i + 1]
            prob = norm.cdf(b) - norm.cdf(a)
            if prob > 1e-10:
                centroids[i] = (norm.pdf(a) - norm.pdf(b)) / prob
            else:
                centroids[i] = (a + b) / 2

        new_boundaries = np.zeros(n_levels + 1)
        new_boundaries[0] = -np.inf
        new_boundaries[-1] = np.inf
        for i in range(1, n_levels):
            new_boundaries[i] = (centroids[i - 1] + centroids[i]) / 2
        boundaries = new_boundaries

    return centroids


def emit_header(centroids: np.ndarray, boundaries: np.ndarray) -> str:
    lines = [
        "/* Auto-generated by scripts/gen_centroids.py. DO NOT EDIT BY HAND.",
        " *",
        " * Lloyd-Max optimal scalar quantizer for X ~ N(0, 1), 16 levels.",
        " * Algorithm and bit-exact match: PolarQuant arXiv:2603.29078,",
        " * polar_quant.py::_compute_lloyd_max_centroids(n_levels=16, n_iter=100).",
        " *",
        " * Regenerate via:",
        " *   python scripts/gen_centroids.py \\",
        " *     > include/polarquant/polar_centroids.h",
        " */",
        "#ifndef POLARQUANT_POLAR_CENTROIDS_H",
        "#define POLARQUANT_POLAR_CENTROIDS_H",
        "",
        "#ifdef __cplusplus",
        'extern "C" {',
        "#endif",
        "",
        "#define POLAR_Q4_N_LEVELS 16",
        "",
        "/* Centroid values (the 16 reconstruction points for N(0,1)). */",
        "static const float POLAR_Q4_CENTROIDS[POLAR_Q4_N_LEVELS] = {",
    ]
    for i, c in enumerate(centroids):
        comma = "," if i + 1 < len(centroids) else ""
        lines.append(f"    {c: .9e}f{comma}")
    lines.append("};")
    lines.append("")
    lines.append("/* Voronoi cell boundaries (15 internal cuts; the outer two")
    lines.append(" * cells extend to +/- infinity).  Used by the encoder for")
    lines.append(" * O(log N) bucketization instead of an O(N) argmin sweep.")
    lines.append(" */")
    lines.append(
        "static const float POLAR_Q4_BOUNDARIES[POLAR_Q4_N_LEVELS - 1] = {"
    )
    inner = boundaries[1:-1]
    for i, b in enumerate(inner):
        comma = "," if i + 1 < len(inner) else ""
        lines.append(f"    {b: .9e}f{comma}")
    lines.append("};")
    lines.append("")
    lines.append("#ifdef __cplusplus")
    lines.append("}")
    lines.append("#endif")
    lines.append("")
    lines.append("#endif /* POLARQUANT_POLAR_CENTROIDS_H */")
    return "\n".join(lines) + "\n"


def main() -> int:
    centroids = compute_centroids(N_LEVELS, N_ITER)

    # Recompute boundaries one more time so they correspond to the
    # final centroid set.  Same formula as the iteration body.
    boundaries = np.zeros(N_LEVELS + 1)
    boundaries[0] = -np.inf
    boundaries[-1] = np.inf
    for i in range(1, N_LEVELS):
        boundaries[i] = (centroids[i - 1] + centroids[i]) / 2.0

    sys.stdout.write(emit_header(centroids, boundaries))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
