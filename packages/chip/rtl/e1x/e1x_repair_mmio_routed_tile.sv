`include "rtl/e1x/e1x_pkg.sv"

module e1x_repair_mmio_routed_tile #(
  parameter int PORTS = e1x_pkg::E1X_PORTS,
  parameter int COLORS = e1x_pkg::E1X_ROUTING_COLORS,
  parameter int PAYLOAD_BITS = e1x_pkg::E1X_FABRIC_PAYLOAD_BITS,
  parameter int INDEX_BITS = 32,
  parameter int MAX_REMAPS = 16,
  parameter int MAX_ROUTES = 16
) (
  input  logic clk_i,
  input  logic rst_ni,
  input  logic clear_i,
  input  logic core_enable_i,
  input  logic core_instr_valid_i,
  input  logic [31:0] core_instr_i,
  input  logic repair_enable_i,
  input  logic mmio_write_valid_i,
  input  logic [7:0] mmio_write_addr_i,
  input  logic [31:0] mmio_write_data_i,
  output logic mmio_write_ready_o,
  input  logic mmio_read_valid_i,
  input  logic [7:0] mmio_read_addr_i,
  output logic mmio_read_valid_o,
  output logic [31:0] mmio_read_data_o,
  output logic repair_programmer_busy_o,
  output logic repair_programmer_error_o,
  output logic [31:0] repair_programmer_words_pushed_o,
  output logic repair_load_done_o,
  output logic repair_load_error_o,
  output logic repair_overflow_o,
  output logic [31:0] repair_remap_count_o,
  output logic [31:0] repair_route_count_o,
  input  logic [PORTS-1:0] port_disable_i,
  input  logic [COLORS-1:0][PORTS-1:0][2:0] route_table_i,
  input  logic [PORTS-2:0] fabric_valid_i,
  input  logic [PORTS-2:0][$clog2(COLORS)-1:0] fabric_color_i,
  input  logic [PORTS-2:0][PAYLOAD_BITS-1:0] fabric_payload_i,
  input  logic [PORTS-2:0][INDEX_BITS-1:0] fabric_src_logical_i,
  input  logic [PORTS-2:0][INDEX_BITS-1:0] fabric_dst_logical_i,
  input  logic [INDEX_BITS-1:0] local_src_logical_i,
  input  logic [INDEX_BITS-1:0] local_dst_logical_i,
  output logic [PORTS-2:0] fabric_ready_o,
  output logic [PORTS-2:0] fabric_valid_o,
  output logic [PORTS-2:0][$clog2(COLORS)-1:0] fabric_color_o,
  output logic [PORTS-2:0][PAYLOAD_BITS-1:0] fabric_payload_o,
  output logic [31:0] core_pc_o,
  output logic [63:0] core_x1_o,
  output logic [63:0] core_x2_o,
  output logic [63:0] core_x3_o,
  output logic [63:0] core_x10_o,
  output logic core_halted_o,
  output logic core_active_o,
  output logic repaired_drop_o,
  output logic [PORTS-1:0] repair_override_used_o
);
  logic repair_clear;
  logic repair_word_valid;
  logic [63:0] repair_word;
  logic repair_word_ready;

  e1x_repair_mmio_programmer u_programmer (
    .clk_i(clk_i),
    .rst_ni(rst_ni),
    .clear_i(clear_i),
    .mmio_write_valid_i(mmio_write_valid_i),
    .mmio_write_addr_i(mmio_write_addr_i),
    .mmio_write_data_i(mmio_write_data_i),
    .mmio_write_ready_o(mmio_write_ready_o),
    .mmio_read_valid_i(mmio_read_valid_i),
    .mmio_read_addr_i(mmio_read_addr_i),
    .mmio_read_valid_o(mmio_read_valid_o),
    .mmio_read_data_o(mmio_read_data_o),
    .repair_clear_o(repair_clear),
    .repair_word_valid_o(repair_word_valid),
    .repair_word_o(repair_word),
    .repair_word_ready_i(repair_word_ready),
    .busy_o(repair_programmer_busy_o),
    .error_o(repair_programmer_error_o),
    .words_pushed_o(repair_programmer_words_pushed_o)
  );

  e1x_repair_routed_tile #(
    .PORTS(PORTS),
    .COLORS(COLORS),
    .PAYLOAD_BITS(PAYLOAD_BITS),
    .INDEX_BITS(INDEX_BITS),
    .MAX_REMAPS(MAX_REMAPS),
    .MAX_ROUTES(MAX_ROUTES)
  ) u_tile (
    .clk_i(clk_i),
    .rst_ni(rst_ni),
    .clear_i(clear_i | repair_clear),
    .core_enable_i(core_enable_i),
    .core_instr_valid_i(core_instr_valid_i),
    .core_instr_i(core_instr_i),
    .repair_enable_i(repair_enable_i),
    .repair_word_valid_i(repair_word_valid),
    .repair_word_i(repair_word),
    .repair_word_ready_o(repair_word_ready),
    .repair_load_done_o(repair_load_done_o),
    .repair_load_error_o(repair_load_error_o),
    .repair_overflow_o(repair_overflow_o),
    .repair_remap_count_o(repair_remap_count_o),
    .repair_route_count_o(repair_route_count_o),
    .port_disable_i(port_disable_i),
    .route_table_i(route_table_i),
    .fabric_valid_i(fabric_valid_i),
    .fabric_color_i(fabric_color_i),
    .fabric_payload_i(fabric_payload_i),
    .fabric_src_logical_i(fabric_src_logical_i),
    .fabric_dst_logical_i(fabric_dst_logical_i),
    .local_src_logical_i(local_src_logical_i),
    .local_dst_logical_i(local_dst_logical_i),
    .fabric_ready_o(fabric_ready_o),
    .fabric_valid_o(fabric_valid_o),
    .fabric_color_o(fabric_color_o),
    .fabric_payload_o(fabric_payload_o),
    .core_pc_o(core_pc_o),
    .core_x1_o(core_x1_o),
    .core_x2_o(core_x2_o),
    .core_x3_o(core_x3_o),
    .core_x10_o(core_x10_o),
    .core_halted_o(core_halted_o),
    .core_active_o(core_active_o),
    .repaired_drop_o(repaired_drop_o),
    .repair_override_used_o(repair_override_used_o)
  );
endmodule
