#!/usr/bin/env python3
"""Prepare a merged augmented+distilled corpus for finetune_kokoro_full.py.

This takes a corpus dir with train_list.txt + val_list.txt + wavs_norm/
and creates the processed/ subdirectory that finetune_kokoro_full.py expects:

  processed/
    train_list.txt       (copied)
    val_list.txt         (copied)
    wavs_norm/           (symlinked)
    phonemes.jsonl       (generated by phonemizing each transcript)
    prep_manifest.json   (stats)

The phonemes.jsonl maps clip_id → {clip_id, norm_text, raw_text}.
finetune_kokoro_full.py reads phonemes.jsonl and phonemizes at runtime;
this script ensures the clip_id↔text mapping exists.
"""

from __future__ import annotations

import argparse
import hashlib
import json
import logging
import os
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

logging.basicConfig(level=logging.INFO, format="%(asctime)s %(levelname)s %(message)s")
log = logging.getLogger("kokoro.prep_merged")


def _sha256_file(path: Path) -> str:
    h = hashlib.sha256()
    with path.open("rb") as fh:
        for chunk in iter(lambda: fh.read(1 << 20), b""):
            h.update(chunk)
    return h.hexdigest()


def prep_merged_corpus(corpus_dir: Path, run_dir: Path) -> dict[str, Any]:
    processed = run_dir / "processed"
    processed.mkdir(parents=True, exist_ok=True)

    # Symlink wavs_norm
    wavs_src = corpus_dir / "wavs_norm"
    wavs_dst = processed / "wavs_norm"
    if wavs_dst.exists() or wavs_dst.is_symlink():
        if wavs_dst.is_symlink():
            wavs_dst.unlink()
        else:
            import shutil
            shutil.rmtree(str(wavs_dst))
    os.symlink(str(wavs_src.resolve()), str(wavs_dst))
    log.info("symlinked wavs_norm: %s → %s", wavs_src, wavs_dst)

    # Copy train/val lists
    for fname in ("train_list.txt", "val_list.txt"):
        src = corpus_dir / fname
        dst = processed / fname
        if src.exists():
            dst.write_text(src.read_text(encoding="utf-8"), encoding="utf-8")
            log.info("copied %s (%d lines)", fname, dst.read_text().count("\n"))

    # Build phonemes.jsonl from train + val lists
    # The second field in the LJSpeech line is already normalized text.
    # finetune_kokoro_full.py reads phonemes.jsonl for norm_text,
    # then phonemizes at runtime. We just need clip_id → norm_text mapping.
    phonemes_records: list[dict[str, Any]] = []
    seen_ids: set[str] = set()

    for fname in ("train_list.txt", "val_list.txt"):
        list_path = processed / fname
        if not list_path.exists():
            continue
        for line in list_path.read_text(encoding="utf-8").splitlines():
            line = line.strip()
            if not line:
                continue
            parts = line.split("|", 2)
            if len(parts) < 2:
                continue
            wav_rel = parts[0]
            norm_text = parts[1].strip()
            clip_id = Path(wav_rel).stem
            if clip_id in seen_ids:
                continue
            seen_ids.add(clip_id)
            phonemes_records.append({
                "clip_id": clip_id,
                "norm_text": norm_text,
                "raw_text": norm_text,
            })

    phonemes_path = processed / "phonemes.jsonl"
    with phonemes_path.open("w", encoding="utf-8") as fh:
        for rec in phonemes_records:
            fh.write(json.dumps(rec) + "\n")
    log.info("wrote phonemes.jsonl with %d records", len(phonemes_records))

    # Compute total duration estimate
    total_s = 0.0
    n_clips = 0
    try:
        import librosa  # noqa: PLC0415
        wavs_dir = processed / "wavs_norm"
        for wav in list(wavs_dir.glob("*.wav"))[:5]:  # sample for speed
            try:
                dur = librosa.get_duration(path=str(wav))
                total_s += dur
                n_clips += 1
            except Exception:  # noqa: BLE001
                pass
        if n_clips > 0:
            n_total = len(list(wavs_dir.glob("*.wav")))
            total_s = (total_s / n_clips) * n_total
    except ImportError:
        pass

    manifest = {
        "generatedAt": datetime.now(timezone.utc).isoformat(),
        "corpusDir": str(corpus_dir),
        "runDir": str(run_dir),
        "trainClips": len([r for r in phonemes_records]),
        "totalClips": len(phonemes_records),
        "estimatedTotalDurationS": round(total_s, 2),
        "estimatedTotalDurationMin": round(total_s / 60, 2),
    }
    (processed / "prep_manifest.json").write_text(json.dumps(manifest, indent=2) + "\n")
    log.info("prep complete: %d clips estimated %.1f min", len(phonemes_records), total_s / 60)
    return manifest


def build_parser() -> argparse.ArgumentParser:
    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument("--corpus-dir", type=Path, required=True)
    p.add_argument("--run-dir", type=Path, required=True)
    return p


def main(argv: list[str] | None = None) -> int:
    args = build_parser().parse_args(argv)
    summary = prep_merged_corpus(args.corpus_dir, args.run_dir)
    log.info("done: %s", json.dumps(summary))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
