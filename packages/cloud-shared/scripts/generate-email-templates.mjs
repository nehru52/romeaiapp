#!/usr/bin/env node
/**
 * Bundle the email/templates/*.{html,txt} files into a TS module of string
 * constants. The Cloudflare Workers runtime has no filesystem and no usable
 * import.meta.url, so loading templates with fs.readFileSync / fileURLToPath
 * throws there ("path argument must be of type string ... Received undefined").
 * Bundling them as strings makes rendering work in both Node and workerd.
 *
 * Edit the source files under src/lib/email/templates/, then re-run:
 *   node packages/cloud-shared/scripts/generate-email-templates.mjs
 */
import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const emailDir = join(here, "..", "src", "lib", "email");
const templatesDir = join(emailDir, "templates");
const outFile = join(emailDir, "utils", "email-templates.generated.ts");

const files = readdirSync(templatesDir)
  .filter((f) => f.endsWith(".html") || f.endsWith(".txt"))
  .sort();

const entries = files
  .map(
    (f) =>
      `  ${JSON.stringify(f)}: ${JSON.stringify(readFileSync(join(templatesDir, f), "utf-8"))},`,
  )
  .join("\n");

const out = `// GENERATED by scripts/generate-email-templates.mjs — do not edit by hand.
// Edit the source files under email/templates/ then re-run the generator.
// Bundled as strings because the Cloudflare Workers runtime has no filesystem
// and no import.meta.url (fs.readFileSync / fileURLToPath both throw there).

export const EMAIL_TEMPLATES: Record<string, string> = {
${entries}
};
`;

writeFileSync(outFile, out);
console.log(`[generate-email-templates] wrote ${outFile} with ${files.length} templates`);
