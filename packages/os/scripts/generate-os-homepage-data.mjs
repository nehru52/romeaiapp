#!/usr/bin/env node
// Generates OS homepage artifact data from a release manifest.
// Usage: node generate-os-homepage-data.mjs --manifest PATH --output PATH

import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { parseArgs } from "./os-release-lib.mjs";

const args = parseArgs(process.argv.slice(2));
if (!args.manifest || !args.output) {
  console.error(
    "Usage: node generate-os-homepage-data.mjs --manifest PATH --output PATH",
  );
  process.exit(1);
}

const manifestPath = path.resolve(args.manifest);
const outputPath = path.resolve(args.output);

const raw = await readFile(manifestPath, "utf8");
const manifest = JSON.parse(raw);

const channel = manifest?.release?.channel ?? "beta";
const version = manifest?.release?.version ?? "0.0.0";

/** @param {string} manifestKind @param {object|null} target @returns {string} */
function manifestKindToArtifactKind(manifestKind, _target) {
  if (manifestKind === "raw-image") return "iso";
  if (manifestKind === "vm-image") return "ova";
  if (manifestKind === "android-image") return "apk";
  if (manifestKind === "usb-installer") return "desktop-app";
  return "iso";
}

/** @param {object|null} target @returns {string} */
function manifestPlatformToArtifactPlatform(target) {
  const platform = target?.platform ?? "";
  if (/android|cuttlefish/i.test(platform)) return "android";
  if (/linux/i.test(platform)) return "linux";
  if (/macos|apple/i.test(platform)) return "macos";
  if (/windows|win/i.test(platform)) return "windows";
  return "linux";
}

const artifacts = (
  Array.isArray(manifest?.artifacts) ? manifest.artifacts : []
).map((artifact) => ({
  id: artifact.id,
  label: artifact.filename.replace(/\.zst$|\.zip$/, ""),
  description: artifact.notes ?? "",
  platform: manifestPlatformToArtifactPlatform(artifact.target),
  kind: manifestKindToArtifactKind(artifact.kind, artifact.target),
  channel,
  version,
  downloadUrl: artifact.downloadUrl ?? null,
  checksumUrl: null,
  sizeBytes: artifact.sizeBytes ?? null,
  sha256: artifact.sha256 ?? null,
  releaseNotesUrl: null,
  requiresHardware: undefined,
}));

const tsOutput = `// Generated by packages/os/scripts/generate-os-homepage-data.mjs.
// Do not edit by hand — regenerate from the release manifest.

export type OsArtifact = {
  id: string;
  label: string;
  description: string;
  platform: 'linux' | 'android' | 'macos' | 'windows' | 'cross-platform';
  kind: 'iso' | 'deb' | 'ova' | 'apk' | 'desktop-app';
  channel: 'stable' | 'beta' | 'nightly';
  version: string;
  downloadUrl: string | null;
  checksumUrl: string | null;
  sizeBytes: number | null;
  sha256: string | null;
  releaseNotesUrl: string | null;
  requiresHardware?: string;
};

export const osArtifacts: OsArtifact[] = ${JSON.stringify(artifacts, null, 2)};
`;

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, tsOutput, "utf8");
console.log(`Generated ${artifacts.length} OS artifacts → ${outputPath}`);
