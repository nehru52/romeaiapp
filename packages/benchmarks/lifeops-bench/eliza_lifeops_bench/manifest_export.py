def _build_entry(name: str, spec: dict[str, Any]) -> dict[str, Any]:
    """Render one augmented manifest entry."""
    discriminator = _DISCRIMINATORS.get(name)
    properties: dict[str, Any] = {}
    required: list[str] = []
    if discriminator is not None:
        field, values = discriminator
        properties[field] = {
            "type": "string",
            "description": f"LifeOps {name} discriminator.",
            "enum": list(values),
        }
        required.append(field)
    elif spec.get("discriminator_values") is not None:
        # Umbrella has an enum-style ``subaction`` but isn't in the runner
        # ``_DISCRIMINATORS`` table (e.g. ``LIFE_DELETE``). Default to
        # ``subaction`` as the field name.
        properties["subaction"] = {
            "type": "string",
            "description": f"LifeOps {name} discriminator.",
            "enum": list(spec["discriminator_values"]),
        }
        required.append("subaction")
    # Documentation kwargs shared across umbrellas.
    properties.setdefault("intent", {"type": "string"})

    for key, schema in spec.get("extra_properties", {}).items():
        properties.setdefault(key, schema)

    domain = spec["domain"]
    capabilities = spec["capabilities"]
    tags = [domain, *capabilities, "surface:internal"]

    return {
        "type": "function",
        "function": {
            "name": name,
            "description": _TOOL_DESCRIPTIONS.get(
                name, f"Bench umbrella verb for {name}."
            ),
            "parameters": {
                "type": "object",
                "properties": properties,
                "required": required,
                "additionalProperties": False,
            },
            "strict": True,
        },
        "_plugin": "@elizaos/lifeops-bench",
        "_tags": tags,
        "_contexts": ["general"],
        "_priority": 100,
        "_examples_count": 0,
        "_domain": domain,
        "_capabilities": capabilities,
        "_surfaces": ["surface:internal"],
        "_risk": None,
        "_cost": None,
    }


def bench_umbrella_actions() -> list[dict[str, Any]]:
    """Return manifest entries for every bench-only umbrella verb."""
    return [_build_entry(name, spec) for name, spec in _BENCH_UMBRELLA_AUGMENTS.items()]


def augment_manifest(manifest: dict[str, Any]) -> dict[str, Any]:
    """Return a copy of ``manifest`` with bench-only umbrella entries appended.

    Existing entries with the same name are kept first (the plugin-derived
    surface wins). Bench entries are appended only for names the manifest
    does not already cover, and the action list is sorted by name to match
    the TS exporter's ordering.

    P1-8 dedup guard: if the manifest already contains duplicate names (from a
    prior double-invocation of this function), the duplicates are removed before
    bench entries are appended. The first occurrence of each name wins so the
    plugin-derived surface always takes precedence over any bench entry that
    accidentally ended up in the first slot from a prior run.
    """
    # Remove any pre-existing duplicates so idempotent invocations are safe.
    seen_names: set[str] = set()
    deduplicated: list[dict[str, Any]] = []
    for entry in manifest.get("actions", []):
        name = entry.get("function", {}).get("name")
        if name in seen_names:
            continue
        seen_names.add(name)
        deduplicated.append(entry)

    existing = set(seen_names)
    for entry in bench_umbrella_actions():
        name = entry["function"]["name"]
        if name in existing:
            continue
        deduplicated.append(entry)
        existing.add(name)
    deduplicated.sort(key=lambda a: a["function"]["name"])
    out = dict(manifest)
    out["actions"] = deduplicated
    return out


def patch_manifest_file(path: Path) -> int:
    """Augment a manifest JSON file in place. Returns the count of entries added."""
    raw = json.loads(path.read_text(encoding="utf-8"))
    before = len(raw.get("actions", []))
    patched = augment_manifest(raw)
    after = len(patched["actions"])
    path.write_text(
        json.dumps(patched, indent=2, ensure_ascii=False) + "\n",
        encoding="utf-8",
    )
    return after - before


def main(argv: list[str]) -> int:
    if len(argv) < 2:
        default = (
            Path(__file__).resolve().parent.parent / "manifests" / "actions.manifest.json"
        )
        target = default
    else:
        target = Path(argv[1])
    added = patch_manifest_file(target)
    print(f"patched {target} (+{added} bench umbrella entries)")
    return 0


if __name__ == "__main__":
    raise SystemExit(main(sys.argv))
