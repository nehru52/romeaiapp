"""Augment the elizaOS-derived action manifest with bench-only umbrella verbs.

The static manifest at ``manifests/actions.manifest.json`` is generated by the
TypeScript exporter at ``eliza/scripts/lifeops-bench/export-action-manifest.ts``
from the real elizaOS plugins. That exporter sees only the names the planner
actually registers (``OWNER_REMINDERS_CREATE``, ``OWNER_FINANCES_DASHBOARD``,
``PERSONAL_ASSISTANT_BOOK_TRAVEL``, etc.).

The bench runner, however, dispatches a broader umbrella vocabulary against
the same world (``LIFE_CREATE``, ``MONEY_DASHBOARD``, ``BOOK_TRAVEL``,
``HEALTH``, ``SCHEDULED_TASK_CREATE``...) — see ``runner._ACTION_HANDLERS``.
These names are first-class to the bench but invisible to the plugin
manifest, which means the corpus test that gates every ground-truth action
name against the manifest fails until we register them.

This module exposes the bench-only umbrella verbs as standalone manifest
entries derived from ``runner._DISCRIMINATORS`` and ``runner._TOOL_DESCRIPTIONS``
so the gate accepts them. Running this module as a script patches the
on-disk manifest in place; re-running the TS exporter without re-applying
this patch is a known limitation that ``scripts/lifeops-bench/export-action-manifest.ts``
should eventually fold in.
"""

