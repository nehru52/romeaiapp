"""wild_webpage_006 grader — webpage recreation from image."""

from __future__ import annotations

from typing import Any

from claw_eval.graders.base import AbstractGrader
from claw_eval.graders.multimodal_common import MultimodalGraderMixin
from claw_eval.graders.visual_grader import VisualGraderMixin
from claw_eval.models.task import TaskDefinition
from claw_eval.models.trace import DimensionScores, MediaLoad, ToolDispatch, TraceMessage


VISUAL_RUBRIC = """\
You are evaluating a webpage recreation. The candidate image is a screenshot of an HTML page \
generated by an AI agent. Compare it against the following reference description and score \
how well it matches.

## Reference Description

### Overall Layout
White background, centered content with moderate horizontal padding. \
Top to bottom: navigation bar, title section, filter/search section, task card grid.

### Top Navigation Bar
Left: circular colorful logo icon + bold "Claw Eval" brand name. \
Center-right: four nav links — Home, Tasks (highlighted with light blue rounded-rectangle), \
Architecture, Blog. Far right: GitHub button with icon and rounded border.

### Title Section
Large heading: "Task" in bold black + "Browser" in bold blue on the same line. \
Below: small gray text "104 general personal assistant tasks. Click a card to see full query and tools."

### Filter and Search Section
Two pill-shaped filter buttons: "General" (selected, light blue, outlined) and "Multimodal" (unselected, gray). \
Below: rounded search input with gray placeholder "Search tasks...". \
Below that: small text "104 tasks shown".

### Task Card Grid
4-column grid. 4 rows, 16 cards total. Each card is a white rounded rectangle with \
light gray border and subtle shadow. Each card contains:
1. Small gray monospaced task ID (e.g. T01zh_email_triage) with a dropdown arrow on the right
2. Bold black task title (e.g. "Email Triage", "Calendar Scheduling")
3. Two pill tags: language tag (ZH=blue bg, EN=gray/white bg) + category tag \
(communication=pale blue, productivity=green, finance=yellow, knowledge=purple)
4. 1–2 lines of small gray description text, truncated with ellipsis if long

Card arrangement (pairs of ZH/EN per row):
- Row 1: Email Triage (ZH), Email Triage (EN), Calendar Scheduling (ZH), Calendar Scheduling (EN)
- Row 2: Email Reply Draft (ZH), Email Reply Draft (EN), Todo Management (ZH), Todo Management (EN)
- Row 3: Contact Lookup (ZH), Contact Lookup (EN), Expense Report (ZH), Expense Report (EN)
- Row 4: Meeting Notes Action Items (ZH), Meeting Notes Action Items (EN), \
Knowledge Base Search (ZH), Knowledge Base Search (EN)

## Scoring Checklist

Evaluate each key point and compute an overall score:

1. **Navigation bar** (0–1): logo + brand name + nav links (Home/Tasks/Architecture/Blog) + \
GitHub button all present and correctly styled
2. **Title section** (0–1): "Task" in black + "Browser" in blue on same line, \
subtitle text present
3. **Filter section** (0–1): General (selected) + Multimodal pills present, \
search input present, "104 tasks shown" text present
4. **Card grid layout** (0–1): 4-column grid, approximately 16 cards visible
5. **Card internal structure** (0–1): task ID, title, language tag (ZH/EN with correct colors), \
category tag (with correct color coding), description text all present in cards
6. **Overall visual fidelity** (0–1): color palette (white/gray/blue), clean modern style, \
card spacing, rounded corners all consistent with the reference

Final score = average of the 6 checklist items above (a float between 0.0 and 1.0)."""


class WildWebpage006Grader(AbstractGrader, MultimodalGraderMixin, VisualGraderMixin):
    """Grade webpage recreation PNG (1.0).

    - file_exists (0.1): /workspace/output.png saved
    - visual_score (0.9): visual judge compares against reference description
    """

    OUTPUT_FILE = "/workspace/output.png"

    def grade(
        self,
        messages: list[TraceMessage],
        dispatches: list[ToolDispatch],
        task: TaskDefinition,
        audit_data: dict[str, dict] | None = None,
        judge: Any | None = None,
        media_events: list[MediaLoad] | None = None,
        env_snapshot: dict | None = None,
    ) -> DimensionScores:
        scores = DimensionScores()
        scores.safety = 1.0

        file_exists = self.check_file_exists(env_snapshot, self.OUTPUT_FILE)
        completion = 0.1 if file_exists else 0.0

        if file_exists and judge and hasattr(judge, "evaluate_visual"):
            png_entry = (env_snapshot or {}).get(f"file:{self.OUTPUT_FILE}", {})
            png_b64 = (
                png_entry.get("content", "")
                if png_entry.get("encoding") == "base64"
                else ""
            )
            if png_b64:
                result = judge.evaluate_visual(
                    rubric=VISUAL_RUBRIC,
                    reference_images_b64=[],
                    candidate_images_b64=[png_b64],
                    context="Candidate image is a screenshot of an HTML page recreating the Task Browser webpage.",
                )
                visual_score = result.score if result else 0.0
                completion += 0.9 * visual_score

        scores.completion = round(completion, 2)
        scores.robustness = self.compute_robustness(dispatches)
        scores.efficiency_turns = len(
            [m for m in messages if m.message.role == "assistant"]
        )
        return scores
