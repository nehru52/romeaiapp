#!/usr/bin/env node
/**
 * Regenerates `known-phrase.ts` from the committed `known-phrase.wav` — the
 * bundled voice self-test audio fixture, embedded as a base64 `data:` URL so it
 * works in the @elizaos/ui library build and any host bundler with no asset
 * pipeline.
 *
 * `known-phrase.wav` is a REAL-SPEECH 16 kHz mono clip of EXPECTED_PHRASE,
 * synthesized with omnivoice.cpp (the project's llama.cpp-based TTS — NOT ONNX)
 * and downsampled to 16 kHz, so a genuine real-audio -> real-ASR pass works on
 * any host with a provisioned ASR (e.g. the on-device Android agent). To
 * regenerate the clip:
 *
 *   BUILD=plugins/plugin-local-inference/native/omnivoice.cpp/build
 *   MODELS=~/.local/state/eliza/local-inference/models/omnivoice
 *   echo "what time is it" | LD_LIBRARY_PATH=$BUILD $BUILD/omnivoice-tts \
 *     --model $MODELS/omnivoice-base-q4_k_m.gguf \
 *     --codec $MODELS/omnivoice-tokenizer-q4_k_m.gguf --format wav16 -o /tmp/o.wav
 *   ffmpeg -y -i /tmp/o.wav -ar 16000 -ac 1 -c:a pcm_s16le \
 *     packages/ui/src/voice/voice-selftest/fixtures/known-phrase.wav
 *
 * Then run this to re-embed it:
 *   node packages/ui/src/voice/voice-selftest/fixtures/gen-known-phrase.mjs
 */
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const EXPECTED_PHRASE = "what time is it";
const dir = dirname(fileURLToPath(import.meta.url));
const wavPath = join(dir, "known-phrase.wav");

const wav = readFileSync(wavPath);
if (wav.length < 1000 || wav.toString("ascii", 0, 4) !== "RIFF") {
  throw new Error(
    `${wavPath} is not a valid WAV (regenerate via omnivoice.cpp)`,
  );
}
const dataUrl = `data:audio/wav;base64,${wav.toString("base64")}`;
const tsOut = join(dir, "known-phrase.ts");
writeFileSync(
  tsOut,
  `// AUTO-GENERATED by gen-known-phrase.mjs from known-phrase.wav — do not edit.\n` +
    `// Real-speech 16 kHz mono clip of EXPECTED_PHRASE (omnivoice.cpp TTS),\n` +
    `// embedded as a data: URL so it bundles with no asset pipeline.\n` +
    `export const EXPECTED_PHRASE = ${JSON.stringify(EXPECTED_PHRASE)};\n` +
    `export const KNOWN_PHRASE_WAV_DATA_URL =\n  ${JSON.stringify(dataUrl)};\n`,
);
console.log(
  `wrote ${tsOut} (${wav.length} bytes wav, phrase: "${EXPECTED_PHRASE}")`,
);
